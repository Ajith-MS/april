#Compound assignment operators
num1=10
num1+=1 #num1=num1+1    10=10+1= 11
print(num1)

num1=20
num1-=3 #num1=num1-3    20=20-3= 17
print(num1)

num1=10
num1*=5 #num1=num1*5    10=10*5= 50
print(num1)

num1=10
num1/=5 #num1=num1/5    10=10/5= 2
print(num1)

num1=10
num1%=2 #num1=num1%2    10%2= 0
print(num1)