#Relational operators

# >     greater than
# <     less than
# >=    greater than or equalto
# <=    less than or equalto
# ==    equalto equalto
# !=    not equalto

# Relational operators are used for comparison
# Output of the relational operators will be 'True' or 'False'

num1=int(input("Enter number1 :"))
num2=int(input("Enter number2 :"))
print(num1>num2)
print(num1<num2)
print(num1>=num2)
print(num1<=num2)
print(num1==num2)
print(num1!=num2)

