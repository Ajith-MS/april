#Addition
f=lambda num1,num2:num1+num2
print("sum of  2 numbers =",f(10,20))

#Subtraction
f=lambda num1,num2:num1-num2
print("sub of 2 numbers =",f(50,10))

#Multiplication
f=lambda num1,num2:num1*num2
print("product of 2 numbers =",f(40,2))

#Division
f=lambda num1,num2:num1/num2
print("div of 2 numbers =",f(40,5))

#Modulus
f=lambda num1,num2:num1%num2
print("modulus of 2 numbers =",f(40,5))

#Floor division
f=lambda num1,num2:num1//num2
print("flrdiv of 2 numbers =",f(7,3))